public class Uni5Exe01 {
    public static void main(String[] args) {
            for (int num = 0; num <= 20 ; num++){
                    System.out.println("Número " + num);
                    
                    if (num % 2 == 0) {
                        System.out.println("Esse número é Par!");
                     } else {
                        System.out.println("Esse número é Ímpar!");
                     }
                    }
            }
    }